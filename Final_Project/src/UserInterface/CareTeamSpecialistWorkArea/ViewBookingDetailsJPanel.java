    private void btnViewDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailActionPerformed

        int selectedRow = tblPendingWorkRequest.getSelectedRow();

        if (selectedRow >= 0) {
            careTeamLeaderWorkRequest = (CareTeamLeaderWorkRequest) tblPendingWorkRequest.getValueAt(selectedRow, 0);

            NewBookingRequestJPanel nbrjp = new NewBookingRequestJPanel(userProcessContainer, userAccount, organization, enterprise, ecoSystem, careTeamLeaderWorkRequest);
            userProcessContainer.add("New Booking Request JPanel", nbrjp);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewDetailActionPerformed

    private void btnViewRequestDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewRequestDetailActionPerformed

        int selectedRow = tblWorkRequestHistory.getSelectedRow();

        if (selectedRow >= 0) {
            careTeamLeaderWorkRequest = (CareTeamLeaderWorkRequest) tblPendingWorkRequest.getValueAt(selectedRow, 0);

            OldBookingRequestJPanel obrjp = new OldBookingRequestJPanel(userProcessContainer, careTeamLeaderWorkRequest);
            userProcessContainer.add("Old Booking Request JPanel", obrjp);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_btnViewRequestDetailActionPerformed

