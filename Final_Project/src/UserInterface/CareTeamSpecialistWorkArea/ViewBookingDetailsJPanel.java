    private void populatePendingWorkRequest() {

        DefaultTableModel defaultTableModel = (DefaultTableModel) tblPendingWorkRequest.getModel();
        defaultTableModel.setRowCount(0);

        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
            careTeamLeaderWorkRequest = (CareTeamLeaderWorkRequest) request;

            if (careTeamLeaderWorkRequest.getStatus().equalsIgnoreCase(WorkRequest.TypeOfStatus.Pending.getValue())) {
                Object[] row = new Object[4];

                row[0] = careTeamLeaderWorkRequest;
                row[1] = careTeamLeaderWorkRequest.getDoctor().getPerson().getFirstName();
                row[2] = careTeamLeaderWorkRequest.getRequestDate();
                row[3] = careTeamLeaderWorkRequest.getStatus();

                defaultTableModel.addRow(row);
            }

        }
    }

    private void populateWorkRequestHistoryTable() {

        DefaultTableModel defaultTableModel = (DefaultTableModel) tblWorkRequestHistory.getModel();
        defaultTableModel.setRowCount(0);

        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
            careTeamLeaderWorkRequest = (CareTeamLeaderWorkRequest) request;

            if (careTeamLeaderWorkRequest.getStatus().equalsIgnoreCase(WorkRequest.TypeOfStatus.Completed.getValue())) {
                Object[] row = new Object[4];

                row[0] = careTeamLeaderWorkRequest;
                row[1] = careTeamLeaderWorkRequest.getDoctor().getPerson().getFirstName();
                row[2] = careTeamLeaderWorkRequest.getRequestDate();
                row[3] = careTeamLeaderWorkRequest.getStatus();

                defaultTableModel.addRow(row);
            }

        }
    }

