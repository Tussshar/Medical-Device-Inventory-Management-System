    private void populateNetworkTable() {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tblNetwork.getModel();
        defaultTableModel.setRowCount(0);
        for (Network network : ecoSystem.getNetworkList()) {
            Object[] row = new Object[1];
            row[0] = network.getName();
            defaultTableModel.addRow(row);
        }
    }

    private void populateLocationComboBox() {
        cmbLocation.removeAllItems();
        int count = 0;
        for (NetworkLocation.LocationsForNetwork location : NetworkLocation.LocationsForNetwork.values()) {
            for(Network network : ecoSystem.getNetworkList()){
                if(network.getName().equals(location.getValue())){
                    count++;
                    break;
                }
            }
            
            if(count == 0){
                cmbLocation.addItem(location);
            }
            count = 0;
        }
    }

