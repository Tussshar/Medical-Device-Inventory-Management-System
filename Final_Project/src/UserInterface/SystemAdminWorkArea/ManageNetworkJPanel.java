    private void btnCreateNetworkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateNetworkActionPerformed
        NetworkLocation.LocationsForNetwork location = (NetworkLocation.LocationsForNetwork) cmbLocation.getSelectedItem();
        checkNetworkDuplicate = false;
        for (Network network : ecoSystem.getNetworkList()) {
            if (location.getValue().equals(network.getName())) {
                checkNetworkDuplicate = true;
                return;
            }
        }
        if(!checkNetworkDuplicate){
            ecoSystem.createAndAddNetwork(location);
            JOptionPane.showMessageDialog(null, "Network has been created successfully");
            
        }
        else{
            JOptionPane.showMessageDialog(null, "The selected Network Already Exist");
        }
        populateLocationComboBox();
        populateNetworkTable();
    }//GEN-LAST:event_btnCreateNetworkActionPerformed


