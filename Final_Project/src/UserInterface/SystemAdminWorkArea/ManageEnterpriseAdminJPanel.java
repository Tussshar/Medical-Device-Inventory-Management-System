    private void populateNetworkComboBox() {
        cmbNetwork.removeAllItems();

        for (Network network : ecoSystem.getNetworkList()) {
            cmbNetwork.addItem(network);
        }
    }

    private void populateEnterpriseComboBox(Network network) {
        cmbEnterprise.removeAllItems();
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (enterprise.getPersonDirectory().getPersonList().isEmpty()) {
                cmbEnterprise.addItem(enterprise);
            }
        }
    }

    private void populateRoleComboBox(Enterprise enterprise) {
        cmbRole.removeAllItems();

        for (Role role : enterprise.getSupportedRole()) {
            cmbRole.addItem(role);
        }
    }

    private void populateEnterpriseAdminTable() {

        DefaultTableModel defaultTableModel = (DefaultTableModel) tblEnterpriseAdmin.getModel();
        defaultTableModel.setRowCount(0);

        for (Network network : ecoSystem.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()) {
                    Object[] row = new Object[3];
                    row[0] = enterprise.getOrganizationName();
                    row[1] = network.getName();
                    row[2] = userAccount.getUserName();

                    defaultTableModel.addRow(row);
                }
            }
        }
    }

