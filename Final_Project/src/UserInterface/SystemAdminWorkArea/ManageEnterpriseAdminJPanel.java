    private void btnAdminDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdminDetailActionPerformed
        Enterprise enterprise = (Enterprise) cmbEnterprise.getSelectedItem();

        Role role = (Role) cmbRole.getSelectedItem();
        if (enterprise != null && role != null) {

            personDirectory = enterprise.getPersonDirectory().getPersonList();
            userAccountDirectory = enterprise.getUserAccountDirectory().getUserAccountList();
            organization = enterprise;
            if (organization.getUserAccountDirectory().getUserAccountList().isEmpty()) {
                JFrame frame = new JFrame();
                SignUpPersonDetailJDialog supdjd = new SignUpPersonDetailJDialog(frame, true, organization, ecoSystem, personDirectory, userAccountDirectory);
                supdjd.setVisible(true);
            } else {
                populateEnterpriseAdminTable();
                populateNetworkComboBox();
                if (organization instanceof SupplierEnterprise || organization instanceof HospitalEnterprise) {
                    JOptionPane.showMessageDialog(null, "Admin for this Enterprise is already created");
                } else {
                    JOptionPane.showMessageDialog(null, "Admin for this Organization is already created");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select all combo boxes");
        }
    }//GEN-LAST:event_btnAdminDetailActionPerformed

